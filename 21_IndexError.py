# पाइथोनमा IndexError भनेको त्यस्तो त्रुटि हो जुन तपाईंले कुनै सूची (list), ट्युपल (tuple) वा स्ट्रिङमा भएको एलिमेन्टलाई पहुँच गर्न खोज्दा त्यो एलिमेन्टको इन्डेक्स (index) सूचीको दायराभन्दा बाहिर भएको बेलामा आउँछ।

# IndexError को कारणहरू:

# अनुपयुक्त इन्डेक्स: तपाईंले सूचीको लम्बाइभन्दा ठूलो इन्डेक्स प्रयोग गर्नुभयो भने यो त्रुटि आउँछ।
# ऋणात्मक इन्डेक्स: तपाईंले ऋणात्मक इन्डेक्स प्रयोग गर्दा पनि यो त्रुटि आउन सक्छ। ऋणात्मक इन्डेक्सले सूचीको अन्तिमबाट गणना गर्छ, तर यदि त्यो इन्डेक्स सूचीको लम्बाइभन्दा ठूलो भयो भने यो त्रुटि आउँछ।
# खाली सूची: यदि तपाईंले खाली सूचीमा कुनै एलिमेन्ट खोज्न खोज्नुभयो भने पनि यो त्रुटि आउँछ।
# IndexError को उदाहरण:

# उदाहरण १: सूचीको लम्बाइभन्दा ठूलो इन्डेक्स प्रयोग गर्ने
my_list = [1, 2, 3]
value = my_list[3]  # IndexError: list index out of range

# उदाहरण २: ऋणात्मक इन्डेक्स प्रयोग गर्ने
my_list = [1, 2, 3]
value = my_list[-4]  # IndexError: list index out of range

# उदाहरण ३: खाली सूचीमा एलिमेन्ट खोज्ने
my_list = []
value = my_list[0]  # IndexError: list index out of range