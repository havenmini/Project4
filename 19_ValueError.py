# पाइथोनमा ValueError भनेको त्यस्तो त्रुटि हो जुन तपाईंले कुनै फङ्क्सन वा मेथडलाई अनुपयुक्त मान (value) पास गर्दा आउँछ। 
# यसको अर्थ, तपाईंले कुनै फङ्क्सनलाई त्यसले बुझ्न सक्ने प्रकारको मान दिएनौँ।

# ValueError को कारणहरू:

# अनुपयुक्त डेटा प्रकार: कुनै फङ्क्सनले विशेष प्रकारको डेटा प्रकारको मान मात्र लिन्छ भने तपाईंले फरक प्रकारको मान पास गर्नुभयो भने 
# यस्तो त्रुटि आउन सक्छ। उदाहरणका लागि, int() फङ्क्सनले स्ट्रिङलाई सङ्ख्यामा परिवर्तन गर्छ, तर यदि त्यो स्ट्रिङमा सङ्ख्याबाहेक अरु कुनै अक्षर भएमा ValueError आउँछ।
# अनुपयुक्त मानको दायरा: कुनै फङ्क्सनले निश्चित दायराको मान मात्र लिन्छ भने तपाईंले त्यो दायरा बाहिरको मान पास गर्नुभयो भने पनि ValueError आउन सक्छ। 
# उदाहरणका लागि, sqrt() फङ्क्सनले ऋणात्मक सङ्ख्याको वर्गमूल निकाल्न सक्दैन।
# अनुपयुक्त मानको संख्या: कुनै फङ्क्सनले निश्चित सङ्ख्यामा मान लिन्छ भने तपाईंले धेरै वा कम मान पास गर्नुभयो भने पनि ValueError आउन सक्छ।
# ValueError को उदाहरण:

# उदाहरण १: int() फङ्क्सनमा अक्षर पास गर्ने
number = int("hello")  # ValueError: invalid literal for int() with base 10: 'hello'

# उदाहरण २: sqrt() फङ्क्सनमा ऋणात्मक सङ्ख्या पास गर्ने
import math
result = math.sqrt(-4)  # ValueError: math domain error

# उदाहरण ३: list.index() फङ्क्सनमा नभएको मान खोज्ने
my_list = [1, 2, 3]
index = my_list.index(4)  # ValueError: 4 is not in list