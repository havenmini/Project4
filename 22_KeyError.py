# पाइथोनमा KeyError भनेको त्यस्तो त्रुटि हो जुन तपाईंले कुनै डिक्सनरी (dictionary) मा भएको कुञ्जी (key) खोज्दा त्यो कुञ्जी डिक्सनरीमा नभएको बेलामा आउँछ।

# KeyError को कारणहरू:

# अस्तित्वमा नभएको कुञ्जी: तपाईंले डिक्सनरीमा भएको कुञ्जीको नाम गलत लेख्नुभयो वा त्यो कुञ्जी नै डिक्सनरीमा छैन।
# केस सेन्सेटिभ: पाइथोनमा कुञ्जीहरू केस सेन्सेटिभ हुन्छन्। अर्थात्, 'key' र 'Key' फरक कुञ्जी मानिन्छन्।
# डिक्सनरी खाली छ: यदि डिक्सनरीमा कुनै कुञ्जी छैन भने कुनै पनि कुञ्जी खोज्दा KeyError आउनेछ।
# KeyError को उदाहरण:


# उदाहरण १: अस्तित्वमा नभएको कुञ्जी
my_dict = {'name': 'John', 'age': 30}
value = my_dict['city']  # KeyError: 'city'

# उदाहरण २: केस सेन्सेटिभ
my_dict = {'Name': 'John', 'age': 30}
value = my_dict['name']  # KeyError: 'name'

# उदाहरण ३: खाली डिक्सनरी
my_dict = {}
value = my_dict['any_key']  # KeyError: 'any_key'