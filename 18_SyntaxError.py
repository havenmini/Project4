# पाइथोनमा सिण्ट्याक्स त्रुटि (Syntax Error) भनेको पाइथोन भाषाको नियमहरूको उल्लङ्घन गर्दा आउने त्रुटि हो। यसको अर्थ, तपाईंले पाइथोनको उचित वाक्यविन्यास (syntax) प्रयोग गर्नुभएको छैन।

# सिण्ट्याक्स त्रुटि किन आउँछ?

# कीवर्डहरूको गलत प्रयोग: पाइथोनमा केही विशेष शब्दहरू (जस्तै: if, else, for, while, def, class आदि) छन् जसलाई कीवर्ड भनिन्छ। यी शब्दहरूलाई भेरिएबलको नाम वा अरु कुनै ठाउँमा प्रयोग गर्न मिल्दैन।
# कोलन (:) को गलत प्रयोग: पाइथोनमा कतिपय ठाउँमा कोलन (:) प्रयोग गर्नुपर्छ। जस्तै: if, else, for, while, def, class आदि स्टेटमेन्टहरू पछि।
# कोष्ठकहरूको गलत प्रयोग: कोष्ठकहरू ((), {}, []) को प्रयोगमा गल्ती हुँदा पनि सिण्ट्याक्स त्रुटि आउँछ।
# इनडेन्टेसन (indentation) को गलत प्रयोग: पाइथोनमा कोडलाई सही तरिकाले इन्डेन्ट गर्नुपर्छ। इन्डेन्टेसनले कोडको ब्लकलाई जनाउँछ।
# अतिरिक्त वा कम स्पेस: पाइथोनमा स्पेसको पनि महत्त्व हुन्छ। अनावश्यक स्पेस वा कम स्पेसले पनि सिण्ट्याक्स त्रुटि निम्त्याउन सक्छ।
# अन्त्यमा सेमीकोलन (;) को प्रयोग: पाइथोनमा प्रत्येक लाइनको अन्त्यमा सेमीकोलन प्रयोग गर्न आवश्यक पर्दैन। यो अन्य भाषा जस्तै C, C++ मा चाहिन्छ।
# सिण्ट्याक्स त्रुटिको उदाहरण:


if "x" == 5:
    print("x is 5")

# माथिको कोडमा if स्टेटमेन्ट पछि कोलन (:) छैन, त्यसैले यो एक सिण्ट्याक्स त्रुटि हो।