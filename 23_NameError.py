# पाइथोनमा NameError भनेको त्यस्तो त्रुटि हो जुन तपाईंले कुनै भेरिएबल, फङ्क्सन वा क्लासको नाम प्रयोग गर्नुहुन्छ, तर त्यो नाम अघि परिभाषित गरिएको छैन। अर्थात्, तपाईंले कुनै नाम प्रयोग गर्नुभएको छ जुन पाइथोन इण्टरप्रिटरले पहिचान गर्न सक्दैन।

# NameError को कारणहरू:

# अपरिभाषित भेरिएबल: तपाईंले कुनै भेरिएबललाई मान असाइन गरे बिना नै त्यसलाई प्रयोग गर्नुभयो।
# अपरिभाषित फङ्क्सन: तपाईंले कुनै फङ्क्सनलाई परिभाषित गरे बिना नै त्यसलाई कल गर्नुभयो।
# अपरिभाषित क्लास: तपाईंले कुनै क्लासलाई परिभाषित गरे बिना नै त्यसको इन्स्ट्यान्स बनाउन खोज्नुभयो।
# भेरिएबलको नाममा टाइपो: तपाईंले भेरिएबलको नाम गलत लेख्नुभयो।
# स्थानीय र ग्लोबल स्कोप: भेरिएबलको स्कोप (scope) बुझ्न नसक्दा पनि NameError आउन सक्छ।
# NameError को उदाहरण:

# उदाहरण १: अपरिभाषित भेरिएबल
# print(x)  # NameError: name 'x' is not defined

# उदाहरण २: अपरिभाषित फङ्क्सन
# my_function()  # NameError: name 'my_function' is not defined

# # उदाहरण ३: भेरिएबलको नाममा टाइपो
# age = 30
# print(Age)  # NameError: name 'Age' is not defined